package deneme_1;

public class C04WhileLoop01 {
    public static void main(String[] args) {


   /*
   for(initial index   ;  Condition   ;   increment/decrement){
        code to br executed
        }

        While Loop :
        initial index;

         while( Condition){
         code to be executed;
         increment/decrement;
         }
    */

        //Type numbers from 4 to 14 on console

        for(int i = 4; i<15; i++){
            System.out.print(i +" ");
        }

        //while loop

        int k= 4;
        while (k<15){
            System.out.print(k +" ");
            k++;
        }


    }



}

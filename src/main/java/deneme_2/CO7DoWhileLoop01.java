package deneme_2;

public class CO7DoWhileLoop01 {

    public static void main(String[] args) {

        /*
        While Loop:

        int i = 1;
        while(condition){
        code;
        increement/decr.;
        }

        Do While:

        int i=1;
        do{
        code
        increment/decr. ;
        }
        while(condition);
         */

        int i = 1;
        while(i<1){
            System.out.println("I am while loop...");
             i++;
        }

        int k = 1;
        do{
            System.out.println("I am Do While Loop...");
            k++;
        } while (k<1);
    }
}
